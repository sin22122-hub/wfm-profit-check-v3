export function getProblemAdvice(problem, index = 0) {
  const item = normalize(problem);
  const match = problemRules.find((rule) => has(item, rule.keywords));
  if (match) return { ...match, source: item || match.title };
  return {
    category: '經營焦點',
    severity: index === 0 ? '★★★★☆' : '★★★☆☆',
    title: item || '目前無明顯問題',
    summary: item.includes('無明顯') ? '目前沒有明顯單一風險，但仍需要持續追蹤獲利、回流與金流成本。' : '這項問題會影響獲利穩定度，建議先釐清它與營收、成本、顧客回流之間的關係。',
    cause: '目前資料訊號尚未足以拆出單一原因，建議先將它轉成可追蹤指標。',
    risk: '若沒有轉成可追蹤指標，容易一直知道問題，卻沒有真正改善。',
    action: '先設定一個 30 天內可以追蹤的改善指標，並安排第一個執行動作。',
    next: '每週固定檢查一次進度，避免改善動作停留在想法。',
    quote: '先把問題變成指標，才有辦法把指標變成改善。',
    source: item,
  };
}

export function getStrengthAdvice(strength, index = 0) {
  const item = normalize(strength);
  if (!item || has(item, ['無明顯', '目前無'])) return { ...placeholderStrengths[Math.min(index, placeholderStrengths.length - 1)], source: item || '目前無明顯優勢' };
  const match = strengthRules.find((rule) => has(item, rule.keywords));
  if (match) return { ...match, source: item };
  return {
    category: '優勢放大',
    title: item,
    potential: index === 0 ? '★★★★☆' : '★★★☆☆',
    summary: '這項優勢是下一階段成長支點，建議把它標準化，讓它不只依賴個人經驗。',
    leverage: '可轉成流程、話術、內容或會員制度，形成可複製的經營資產。',
    action: '先定義這項優勢背後的成功做法，並整理成可重複執行的流程。',
    quote: '真正的優勢不是做得到一次，而是能被穩定複製。',
    source: item,
  };
}

export function stageProfile(stage, score) {
  const s = Number(score || 0);
  const text = normalize(stage);
  if (text.includes('擴張') || s >= 85) return {
    headline: '目前已具備放大條件，下一步不是再靠個人努力，而是建立可複製的系統。',
    focus: '複製成功方法、建立團隊分工、放大高價值顧客來源。',
    avoid: '不要在沒有 SOP 的情況下快速擴張，否則管理成本會吃掉成長成果。',
  };
  if (text.includes('優化') || s >= 70) return {
    headline: '目前已接近穩定經營，接下來要把成效好的地方變成流程。',
    focus: '優化回流、提高轉換、放大毛利好的服務與內容。',
    avoid: '不要只增加新客，卻忽略老客與服務組合的獲利效率。',
  };
  if (text.includes('建構') || s >= 55) return {
    headline: '目前已有成長基礎，但仍需要把獲利、會員與內容系統建立起來。',
    focus: '建立回流機制、固定內容節奏與每月獲利追蹤。',
    avoid: '不要同時處理太多問題，先把一個關鍵流程做穩。',
  };
  if (text.includes('修復') || s >= 40) return {
    headline: '目前營運已有基礎，但獲利或回流結構仍需先修復，避免越成長越吃力。',
    focus: '先修復淨利、毛利、回流或固定成本壓力。',
    avoid: '不要急著加大廣告，否則可能只是更快放大目前的漏洞。',
  };
  return {
    headline: '目前需要先回到基本盤，把營收、成本與顧客回流重新整理清楚。',
    focus: '先止血、控成本、建立最基本的回訪與營收追蹤。',
    avoid: '不要先追求擴張或複雜工具，先讓店回到穩定現金流。',
  };
}

export function executiveSummary({ stage, score, problem1, strength1, firstPriority }) {
  const profile = stageProfile(stage, score);
  const problem = getProblemAdvice(problem1, 0);
  const strength = getStrengthAdvice(strength1, 0);
  return {
    headline: profile.headline,
    focus: profile.focus,
    avoid: profile.avoid,
    risk: problem.quote,
    opportunity: strength.quote,
    next: `90 天內先聚焦「${firstPriority || problem.action}」，讓改善方向收斂，不要一次處理太多議題。`,
  };
}
